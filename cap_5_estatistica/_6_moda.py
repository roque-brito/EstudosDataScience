def moda(x: List[float]) -> List[float]:
    '''Retorna uma lista com os valores mais frequentes, 
       pois deve haver mais de uma "moda"'''
    count = Counter(x)
    max_count = max(count.values())
    return [x_i for x_i, count in count.items()
            if count == max_count]

